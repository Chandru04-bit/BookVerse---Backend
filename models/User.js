import mongoose from "mongoose";

// ---------------------------
// User Schema
// ---------------------------
const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true
    },
    password: {
      type: String,
      required: true
    },
    role: {
      type: String,
      enum: ["user", "admin"],
      default: "user"
    }
  },
  { timestamps: true }
);

// ---------------------------
// Export Model
// ---------------------------
const User = mongoose.models.User || mongoose.model("User", userSchema);
export default User;
